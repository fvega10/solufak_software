    public void calcularTotalBarra(){
        DecimalFormat decimales = new DecimalFormat("0.00");
        String dato = this.txtDescuento.getModel().getValue().toString(); 
        double descuento = Double.parseDouble(this.txtSubtotal.getText()) * Integer.parseInt(dato) / 100;
        double totalDescuento = Double.parseDouble(this.txtSubtotal.getText()) - descuento;
        double total = ( totalDescuento * (1 + this.impuestoVentas));
        this.txtTotal.setText(decimales.format(total));
    }
    
    public void BuscarArticulo(){
        FrmBuscarArticulo fB = new FrmBuscarArticulo(null, true, conexion);
        fB.setVisible(true);
        if(fB.isAceptar()){
            oArticulo = fB.getArticulo();
            this.txtCodigo.setText(oArticulo.getCodigo_articulo());
            this.txtDescripcion.setText(oArticulo.getDescripcion());
//            this.txtSubtotal.setText(String.valueOf(oArticulo.getPrecio_venta()));
            this.calcularTotalBarra();
            this.txtDescuento.requestFocus();
            this.txtCodigo.requestFocus(false);
            continuar = true;
        }
    }
    public void BuscarCliente(){
        FrmBuscarCliente fB = new FrmBuscarCliente(null, true, conexion);
        fB.setVisible(true);
        if(fB.isAceptar()){
            if(this.cbxTipoFactura.getSelectedItem().toString().equals("Crédito") && !fB.isClienteCredito()){
                JOptionPane.showMessageDialog(rootPane, "No se permite facturar a crédito");
                this.txtCodigo.setText("");
                this.cbxTipoFactura.requestFocus();
            }else{
                if(fB.isClienteCredito()){
                    ClienteCredito aux = (ClienteCredito)fB.getCliente().get(0);
                    this.txtCodigoCliente.setText(aux.getCodigo());
                    this.txtCodigo.setEnabled(true);
                    this.txtCodigo.requestFocus();
                    continuar = true;
                }else{
                    ClienteContado aux = (ClienteContado)fB.getCliente().get(0);
                    this.txtCodigoCliente.setText(aux.getCodigo());
                    this.txtCodigo.setEnabled(true);
                    this.txtCodigo.requestFocus();
                    continuar = true;
                }
            }
            
        }
    }
    
    public void LlamarCliente(){
        boolean seleccion = false;
        if(!continuar){
            ConsultasSQLTablaClienteContado oC = new ConsultasSQLTablaClienteContado(conexion);
            ArrayList cliente = oC.obtenerClienteContado();
            if(oC.isError()){
                for(int i=0; i<cliente.size(); i++){
                    ClienteContado aux = (ClienteContado)cliente.get(i);
                    if(aux.getCodigo().equals(this.txtCodigoCliente)){
                        oCliente = aux;
                        this.txtCodigo.requestFocus();
                        continuar = false;
                        break;
                    }else{
                        continuar = true;
                    }
                }
                if(seleccion){
                    JOptionPane.showMessageDialog(rootPane, "Este cliente no existe");
                }
            }
        }
    }
    
    public void LlamarArticuloBarra(){
        boolean seleccion = false;
        if(!continuar){
            ConsultasSQLTablaArticulo oC  = new ConsultasSQLTablaArticulo(conexion);
            ArrayList articulo = oC.obtenerArticulo();
            for(int i = 0; i<articulo.size(); i++){
                Articulo oArt = (Articulo)articulo.get(i);
                if(oArt.getCodigo_articulo().equals(this.txtCodigo.getText())){
                    oArticulo = oArt;
                    this.txtCodigo.setText(oArticulo.getCodigo_articulo());
                    this.txtDescripcion.setText(oArticulo.getDescripcion());
//                    this.txtSubtotal.setText(String.valueOf(oArticulo.getPrecio_venta()));
                    this.calcularTotalBarra();
                    this.txtDescuento.requestFocus();
                    continuar = false;
                    break;
                }else{
                    continuar = true;
                }
            }
            if(seleccion){
                JOptionPane.showMessageDialog(rootPane, "Este artículo no existe");
                this.txtCodigo.setText("");
                this.txtDescripcion.setText("");
                this.txtSubtotal.setText("");
                this.txtDescuento.getModel().setValue(0);
                this.txtTotal.setText("");
                this.txtCodigo.requestFocus();
            }
        }else{
            if(!this.txtCodigo.getText().equals("")){
                this.txtDescripcion.setText(oArticulo.getDescripcion());
//                this.txtSubtotal.setText(String.valueOf(oArticulo.getPrecio_venta()));
                this.calcularTotalBarra();
                this.txtDescuento.requestFocus();
                continuar = false;
            }else{
                JOptionPane.showMessageDialog(rootPane, "Debe digitar el código, o presiona F1 para buscar");
                
            }
        }
    }
    
