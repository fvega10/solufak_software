    public void restarFactura(){
        boolean continuar = false;
        int i = 0;
        int j = 0;
        int o = 0;
        int h = 0;
        int fila = this.panelDeDatos.getSelectedRow();
        String codigoEliminar = this.datos[fila][1];
        oFactura.restarDatos(codigoEliminar, fila);
        this.datos[fila][0] = "";
        this.datos[fila][1] = "";
        this.datos[fila][2] = "";
        this.datos[fila][3] = "";
        this.datos[fila][4] = "";
        this.datos[fila][5] = "";
        this.datos[fila][6] = "";
        
        int filaDatos = this.datos.length;
        String[][] aux = new String[filaDatos-1][7];
        
        do{
            do{
                if(!this.datos[i][j].equals("")){
                    aux[o][h] = this.datos[i][j];
                    h++;
                    continuar = true;
                }
                j++;
            }while(j<this.datos[i].length);
            if(continuar){
                h=0;
                o++;
            }
            continuar = false;
            j=0;
            i++;
        }while(i < this.datos.length);
            
        
        this.datos = aux;
        this.modelo.setDataVector(datos, cabecerasVenta); // Se prepara el panel con los datos y cabecera determinados
        this.panelDeDatos.setModel(modelo);
        this.cpSubtotalFacturaGeneral.setText(String.valueOf(oFactura.calcularSubtotal()));
        this.cpDescuentoFacturaGeneral.setText(String.valueOf(oFactura.calcularDescuento()));
        this.cpImpuestoFacturaGeneral.setText(String.valueOf(oFactura.calculoImpuestoVentas()));
        this.cpTotalFacturaGeneral.setText(String.valueOf(oFactura.calcularTotal()));
        
    }
    
    public void calculoTotal(){
        DecimalFormat decimales = new DecimalFormat("0.00");
        String codigo = this.txtCodigo.getText();
        String descripcion = this.txtDescripcion.getText();
        int cantidad = Integer.parseInt(this.txtCantidad.getModel().getValue().toString());
        double subtotal = Double.parseDouble(this.txtSubtotal.getText()) * cantidad;
        double descuento = subtotal * (Double.parseDouble(this.txtDescuento.getModel().getValue().toString())/100);
        double impuesto = (subtotal-descuento) * this.impuestoVentas;
        double total = (subtotal-descuento)+impuesto;
        
//        Articulo nA = new Articulo(cantidad, codigo, descripcion, subtotal, descuento, impuesto, total);
        
        if(!this.txtCodigo.getText().equals("") || !this.txtDescripcion.getText().equals("") || 
            !this.txtSubtotal.getText().equals("") || !this.txtTotal.getText().equals("")){
            
            this.redimensionarMatriz();        
            this.datos[this.datos.length - 1][0] = String.valueOf(cantidad);
            this.datos[this.datos.length - 1][1] = codigo;
            this.datos[this.datos.length - 1][2] = descripcion;
            this.datos[this.datos.length - 1][3] = String.valueOf(subtotal);
            this.datos[this.datos.length - 1][4] = String.valueOf(descuento);
            this.datos[this.datos.length - 1][5] = String.valueOf(impuesto);
            this.datos[this.datos.length - 1][6] = decimales.format(total);
        
        }else{
            JOptionPane.showMessageDialog(rootPane, "Sucedió un error, vuelva a digitar todos los campos");
        }
        this.modelo.setDataVector(datos, cabecerasVenta); // Se prepara el panel con los datos y cabecera determinados
        this.panelDeDatos.setModel(modelo); 
//        this.sumarFactura(nA);
        this.limpiarBarra();
        this.txtCodigo.requestFocus(true);
        this.txtCodigo.requestFocus();
        this.txtCantidad.getModel().setValue(1);
    }
    
