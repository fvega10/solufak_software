    private void btnBorrarArticuloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarArticuloActionPerformed
        // TODO add your handling code here:
        if(this.datos.length>0){                                 // Valida que hayan datos en el table
                int fila = this.panelDeDatos.getSelectedRow();            // Obtiene la fila seleccionada
                if(fila >=0){                                        // Valida que haya una fila selecciona
                    
                    int respuesta = JOptionPane.showConfirmDialog(this, 
                           "¿Está seguro de borrar el registro?", "Borrar", JOptionPane.YES_NO_OPTION); // Muestra un mensaje de alerta
                    // En caso de presionar Si
                    if ( respuesta == JOptionPane.YES_OPTION ) { 
                        try{
                            this.restarFactura();
                            JOptionPane.showMessageDialog(rootPane, "Registro borrado exitosamente");
                        }catch(Exception e){
                            JOptionPane.showMessageDialog(rootPane, "Ha sucedido un error, no se borró la fila");
                        }
                    }
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Debe de seleccionar la fila que desea eliminar");
                }
        }else{
            JOptionPane.showMessageDialog(rootPane, "Acción inválida, no hay datos en el panel");
        }
    }//GEN-LAST:event_btnBorrarArticuloActionPerformed

